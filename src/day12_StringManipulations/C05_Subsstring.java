package day12_StringManipulations;

public class C05_Subsstring {

    public static void main(String[] args) {
        String input= "Veli";
        System.out.print(input.substring(3));
        System.out.print(input.substring(2));
        System.out.print(input.substring(1));
        System.out.print(input.substring(0));
        System.out.print(input.substring(3,3));
    }
}
